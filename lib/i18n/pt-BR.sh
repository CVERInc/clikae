# Portuguese pluralises: tanque/tanques, motor/motores — override the English summary.
i18n_summary() {
  local n="$1" m="$2"
  printf '%s tanque%s em %s motor%s' \
    "$n" "$([ "$n" = 1 ] || echo s)" "$m" "$([ "$m" = 1 ] || echo es)"
}
