# German pluralises Tank/Engine with a plain -s, but "across" needs rephrasing —
# override the English summary ("3 Tanks für 2 Engines").
i18n_summary() {
  local n="$1" m="$2"
  printf '%s Tank%s für %s Engine%s' \
    "$n" "$([ "$n" = 1 ] || echo s)" "$m" "$([ "$m" = 1 ] || echo s)"
}
