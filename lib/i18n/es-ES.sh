# Spanish pluralises both nouns — override the English summary.
i18n_summary() {
  local n="$1" m="$2" t="tanques" e="motores"
  [ "$n" = 1 ] && t="tanque"
  [ "$m" = 1 ] && e="motor"
  printf '%s %s en %s %s' "$n" "$t" "$m" "$e"
}
